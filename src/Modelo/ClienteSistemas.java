package Modelo;

/**
 *
 * @author Usuario
 */
public class ClienteSistemas {
       private int id;
   private int dni;
   private String nombre;
   private String email;
   private int edad;
   private String estadoCivil;
   private String sexo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getDni() {
        return dni;
    }

    public void setDni(int dni) {
        this.dni = dni;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getEstadoCivil() {
        return estadoCivil;
    }

    public void setEstadoCivil(String estadoCivil) {
        this.estadoCivil = estadoCivil;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

    @Override
    public String toString() {
        return "ClienteSistemas{" + "id=" + id + ", dni=" + dni + ", nombre=" + nombre + ", email=" + email + ", edad=" + edad + ", estadoCivil=" + estadoCivil + ", sexo=" + sexo + '}';
    }

}
